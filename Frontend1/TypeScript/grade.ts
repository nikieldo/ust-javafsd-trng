var grade:string='A';
switch(grade) {
    case 'A': {
        console.log("Excellent");
        break;
    }
    case 'B': {
        console.log("good");
        break;
    }
    case 'C': {
        console.log("fair");
        break;
    }
    default: {
        console.log("invalid choice");
        break;
    }
}