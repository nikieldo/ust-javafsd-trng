var myfun1 = function show(){
    console.log("It is a Function Expression")
}

//Anonymous function

var myfun2 = function (){
    console.log("It is an Anonymous Function")
}

//Arrow function

var myfun3 = () =>{
    console.log("It is an Arrow Function")
}

myfun1()
myfun2()
myfun3()